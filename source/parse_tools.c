#include "../header/cub3d.h"

int			namecheck_xpm(char *str, char *xpm)
{
	int		i;

	i = ft_strlen(str);
	if ((i > 4 && str[i - 1] == xpm[2] && str[i - 2] == xpm[1])
		&& (str[i - 3] == xpm[0] && str[i - 4] == '.'))
		return (1);
	return (0);
}
